#!/usr/bin/env python3
""" Module Basic annotations - add """


def add(a: float, b: float) -> float:
    """ 
    Function that take tow float as
    arguments and returns their sum as a float.
    
    Args:
        a (float): first number
        b (float): second number
        
    Returns:
        float: The sum of two floats.
    """
    return a + b
